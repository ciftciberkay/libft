#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	b;
	int	o;

	i = 0;
	b = 0;
	o = 1;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			o = -1;
		i++;
	}
	while ((str[i] <= '9' && str[i] >= '0'))
	{
		b = (str[i] - 48) + (b * 10);
		i++;
	}
	return (b * o);
}
